import math

def addition(a, b):
  "Fonction pour l'addition"
  return a + b

def soustraction(a, b):
  "Fonction pour la soustraction"
  return a - b

def multiplication(a, b):
  "Fonction pour la multiplication"
  return a * b

def division(a, b):
  "Fonction pour la division"
  return a / b



